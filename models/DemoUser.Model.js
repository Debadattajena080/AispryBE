import mongoose, { Schema } from "mongoose";

const demoUserSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
  subject: {
    type: String,
    required: true,
  },
});

const DemoUser = mongoose.model("DemoUser", demoUserSchema);
export default DemoUser;
